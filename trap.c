void
trap(struct trapframe *tf)
{
  if(tf->trapno == T_SYSCALL){
    if(myproc()->killed)
      exit();
    myproc()->tf = tf;
    syscall();
    if(myproc()->killed)
      exit();
    return;
  }

  switch(tf->trapno){
  case T_IRQ0 + IRQ_TIMER:
    if(cpuid() == 0){
      acquire(&tickslock);
      ticks++;
      wakeup(&ticks);
      release(&tickslock);
    }
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_IDE:
    ideintr();
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_IDE+1:
    // Bochs generates spurious IDE1 interrupts.
    break;
  case T_IRQ0 + IRQ_KBD:
    kbdintr();
    lapiceoi();
    break;
  case T_IRQ0 + IRQ_COM1:
    uartintr();
    lapiceoi();
    break;
  case T_IRQ0 + 7:
  case T_IRQ0 + IRQ_SPURIOUS:
    cprintf("cpu%d: spurious interrupt at %x:%x\n",
            cpuid(), tf->cs, tf->eip);
    lapiceoi();
    break;

    case T_PGFLT:
        bad_addr = rcr2();
          // TASK4 data...
       count_pagefaults();
      if(myproc()->pid > 2){
        // TODO: TASK4: add to pagefault counter
        // TASK1: Fail with exit code 13 if it's locked
        if(PM_LOCKED(get_flags(PGROUNDDOWN(bad_addr)))){
            cprintf("access locked page!\n"); //TODO DEBUG ONLY
          tf->eax = T_GPFLT;
          tf->err = T_GPFLT; // does "exit code" mean return value (eax) or error code (err) ?
          // NO BREAK! CONTINUE TO DEFAULT CASE IF NEEDED
        }

        // TASK2: check if we got TRAP14 because it was paged' out.
        //        if it was paged' out, swap it in and continue without panic. the proc will retry in userspace.
        else if(PAGEDOUT(get_flags(PGROUNDDOWN(bad_addr)))){
          // Page was swapped OUT. page it in.
          //cprintf("Pagedout! trap trying to page it in...!\n"); //TODO DEBUG ONLY
          if(swap_in(PGROUNDDOWN(bad_addr))){
              return;
          }
            else{
              // if we can't swap-in and we should be, panic.
            // if NONE was set, we'll skip to the default case as there is no break :)
              #ifndef NONE
              panic("swap in @ pagefault failed!");
              #endif
          }

        }
      }
          // NO BREAK! CONTINUE TO DEFAULT CASE IF NEEDED


          //PAGEBREAK: 13
  default:
    if(myproc() == 0 || (tf->cs&3) == 0){
      // In kernel, it must be our mistake.
      cprintf("unexpected trap %d from cpu %d eip %x (cr2=0x%x)\n",
              tf->trapno, cpuid(), tf->eip, rcr2());
      panic("trap");
    }
    // In user space, assume process misbehaved.
    cprintf("pid %d %s: trap %d err %d on cpu %d "
            "eip 0x%x addr 0x%x--kill proc\n",
            myproc()->pid, myproc()->name, tf->trapno,
            tf->err, cpuid(), tf->eip, rcr2());
    myproc()->killed = 1;
  }

  // Force process exit if it has been killed and is in user space.
  // (If it is still executing in the kernel, let it keep running
  // until it gets to the regular system call return.)
  if(myproc() && myproc()->killed && (tf->cs&3) == DPL_USER)
    exit();

  // Force process to give up CPU on clock tick.
  // If interrupts were on while locks held, would need to check nlock.
  if(myproc() && myproc()->state == RUNNING &&
     tf->trapno == T_IRQ0+IRQ_TIMER)
    yield();

  // Check if the process has been killed since we yielded
  if(myproc() && myproc()->killed && (tf->cs&3) == DPL_USER)
    exit();
}
